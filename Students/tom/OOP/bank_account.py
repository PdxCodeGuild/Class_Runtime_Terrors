class BankAccount:
    def __init__(self, accountnumber, name, balance):
        self.accountnumber = accountnumber
        self.name = name
        self.balance = balance

    def deposit(self):
        self.balance += deposit
        return self.deposit
    
    def bankfees(self):
        bankfees = int(self.balance * 0.05)
        return self.bankfees
    
    def display(self):
        return self.display
